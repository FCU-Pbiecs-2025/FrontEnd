<template>
  <div class="banner-manager-page">
    <div v-if="!isEditPage" class="banner-card">
      <div class="title-row">
        <img src="https://img.icons8.com/ios/48/2e6fb7/image.png" class="icon" alt="icon" />
        <span class="main-title">前台海報管理</span>
      </div>

      <div class="query-card">
        <div class="query-row">
          <div class="search-area">
            <label class="date-label">顯示日期：</label>
            <div class="date-range">
              <input type="date" v-model="dateStart" class="date-input" />
              <span class="to-label">至</span>
              <input type="date" v-model="dateEnd" class="date-input" />
            </div>
          </div>
        </div>
        <div class="btn-query">
          <button class="btn query" @click="doQuery">查詢</button>
        </div>
      </div>

      <div class="table-section">
        <table class="banner-table">
          <thead>
            <tr>
              <th>Banner</th>
              <th>連結</th>
              <th>顯示日期</th>
              <th>狀態</th>
              <th>操作</th>
            </tr>
          </thead>
          <tbody>
            <tr v-for="(b, idx) in resultBanners" :key="b.id">
              <td class="img-cell"><img :src="b.image" alt="banner" /></td>
              <td>{{ b.link }}</td>
              <td>{{ b.displayDate }}</td>
              <td>{{ b.status }}</td>
              <td class="action-cell">
                <button class="btn small" @click="goEdit(b.id)">編輯</button>
                <button class="btn small danger" @click="remove(b.id)">刪除</button>
              </td>
            </tr>
            <tr v-if="resultBanners.length === 0">
              <td colspan="5" class="empty-tip">目前沒有海報</td>
            </tr>
          </tbody>
        </table>
      </div>

      <div class="bottom-row">
        <button class="btn primary" @click="openNew">新增</button>
        <button class="btn primary" v-show="showBack" @click="goBack">返回</button>
      </div>
    </div>
    <router-view v-if="isEditPage" />
  </div>
</template>

<script setup>
import { ref, onMounted, computed } from 'vue'
import { useRouter, useRoute } from 'vue-router'

const router = useRouter()
const route = useRoute()
const storageKey = 'siteBanners'
const banners = ref([])
const resultBanners = ref([])
const showBack = ref(false)

// 日期查詢條件
const dateStart = ref('')
const dateEnd = ref('')

const loadList = () => {
  try {
    const raw = localStorage.getItem(storageKey)
    if (raw) {
      banners.value = JSON.parse(raw)
    } else {
      // 初始範例資料
      banners.value = [
        {
          id: 1,
          image: 'https://via.placeholder.com/800x300/3b82f6/ffffff?text=Banner+1',
          link: '/news',
          displayDate: '2025-01-01',
          status: '顯示'
        },
        {
          id: 2,
          image: 'https://via.placeholder.com/800x300/2563eb/ffffff?text=Banner+2',
          link: '/apply-service',
          displayDate: '2025-01-15',
          status: '顯示'
        },
        {
          id: 3,
          image: 'https://via.placeholder.com/800x300/1e40af/ffffff?text=Banner+3',
          link: '/subsidy-calculator',
          displayDate: '2025-02-01',
          status: '隱藏'
        }
      ]
      localStorage.setItem(storageKey, JSON.stringify(banners.value))
    }
  } catch (e) {
    console.error('loadList error', e)
  }
}

const saveList = () => {
  localStorage.setItem(storageKey, JSON.stringify(banners.value))
}

onMounted(() => {
  loadList()
  resultBanners.value = [...banners.value]
})

const doQuery = () => {
  // 重新載入以確保資料是最新的
  loadList()

  resultBanners.value = banners.value.filter(item => {
    // 日期篩選
    if (dateStart.value && item.displayDate < dateStart.value) return false
    if (dateEnd.value && item.displayDate > dateEnd.value) return false
    return true
  })

  showBack.value = true
}

const openNew = () => {
  router.push({ name: 'AdminBannerNew' })
}

const goEdit = (id) => {
  router.push({ name: 'AdminBannerEdit', params: { id: id } })
}

const remove = (id) => {
  if (!confirm('確定要刪除這筆海報嗎？')) return
  banners.value = banners.value.filter(b => b.id !== id)
  saveList()
  // 重新執行查詢以更新顯示列表
  if (showBack.value) {
    doQuery()
  } else {
    resultBanners.value = [...banners.value]
  }
}

const goBack = () => {
  // 重置查詢條件
  dateStart.value = ''
  dateEnd.value = ''
  // 恢復顯示全部資料
  loadList()
  resultBanners.value = [...banners.value]
  showBack.value = false
}

const isEditPage = computed(() => {
  return route.name === 'AdminBannerNew' || route.name === 'AdminBannerEdit'
})
</script>

